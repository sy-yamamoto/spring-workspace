package com.example.demo.model;

public class Item {
	private String name;
	private int price;

	public Item() {
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public Integer getPrice() {
		return price;
	}

}
